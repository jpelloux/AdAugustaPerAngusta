package fr.iut.adaugustaperangusta.core;

/**
 * Enumeration of Relative Position. Based on cardinal point. Used to represent
 * the direction of a movement.
 *
 * @author jpelloux
 * @version 1.1.0
 */
public enum RelativePos {

	/**
	 * North.
	 */
	NORTH,

	/**
	 * South.
	 */
	SOUTH,

	/**
	 * East.
	 */
	EAST,

	/**
	 * West.
	 */
	WEST;
}
